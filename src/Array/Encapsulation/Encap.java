package Array.Encapsulation;

public class Encap {
    private String name="man";
    private int price=10;
    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return name;
    }
    public void setPrice(int priceo){
        this.price = priceo;
    }
    public int getPrice(){
        return price;
    }



}
